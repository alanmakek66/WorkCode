package com.example.a0321;

public class TranslateList {
    private  String key;
    private  String value;

    public TranslateList(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String replacement) {
        this.value = value;
    }
}
